bool LuaAutocompleteGenerator::generateLSPDefinitions(const Unigine::String& output_path)
{
    LuaSystem* ls = LuaSystem::get();
    if (!ls)
        return false;
    
    sol::state& lua = ls->getState();
    
    std::vector<FunctionSignature> functions;
    std::vector<TypeDefinition> types;
    extractSignatures(lua, functions, types);
    
    std::ofstream file(output_path.get());
    if (!file.is_open())
    {
        Log::error("[Autocomplete] Failed to open file for writing: %s\n", output_path.get());
        return false;
    }
    
    // Generate LSP definition file (sumneko-lua format)
    file << "-- LSP definitions for Unigine Lua API\n";
    file << "-- Auto-generated by LuaAutocompleteGenerator\n\n";
    
    // Write type definitions
    for (const auto& type : types)
    {
        file << "---@class " << type.name << "\n";
        for (const auto& prop : type.properties)
        {
            file << "---@field " << prop << " any\n";
        }
        file << "local " << type.name << " = {}\n\n";
    }
    
    // Write function signatures
    for (const auto& func : functions)
    {
        file << "---" << func.description << "\n";
        file << "---@param ";
        for (size_t i = 0; i < func.parameters.size(); i++)
        {
            if (i > 0) file << " ";
            file << func.parameters[i];
        }
        file << "\n";
        file << "---@return " << func.return_type << "\n";
        file << "function " << func.name << "(";
        for (size_t i = 0; i < func.parameters.size(); i++)
        {
            if (i > 0) file << ", ";
            file << "param" << i;
        }
        file << ") end\n\n";
    }
    
    file.close();
    Log::message("[Autocomplete] Generated LSP definitions: %s\n", output_path.get());
    return true;
}

bool LuaAutocompleteGenerator::generateLuaStubs(const Unigine::String& output_path)
{
    LuaSystem* ls = LuaSystem::get();
    if (!ls)
        return false;
    
    sol::state& lua = ls->getState();
    
    std::vector<FunctionSignature> functions;
    std::vector<TypeDefinition> types;
    extractSignatures(lua, functions, types);
    
    std::ofstream file(output_path.get());
    if (!file.is_open())
    {
        Log::error("[Autocomplete] Failed to open file for writing: %s\n", output_path.get());
        return false;
    }
    
    file << "-- Unigine Lua API Stubs\n";
    file << "-- Auto-generated by LuaAutocompleteGenerator\n\n";
    
    // Generate type stubs
    for (const auto& type : types)
    {
        file << "---@class " << type.name << "\n";
        file << type.name << " = {}\n\n";
        
        for (const auto& method : type.methods)
        {
            file << "---" << method.description << "\n";
            file << "function " << type.name << ":" << method.name << "(";
            for (size_t i = 0; i < method.parameters.size(); i++)
            {
                if (i > 0) file << ", ";
                file << "param" << i;
            }
            file << ") end\n\n";
        }
    }
    
    // Generate global function stubs
    for (const auto& func : functions)
    {
        file << "---" << func.description << "\n";
        file << "function " << func.name << "(";
        for (size_t i = 0; i < func.parameters.size(); i++)
        {
            if (i > 0) file << ", ";
            file << "param" << i;
        }
        file << ") end\n\n";
    }
    
    file.close();
    Log::message("[Autocomplete] Generated Lua stubs: %s\n", output_path.get());
    return true;
}

